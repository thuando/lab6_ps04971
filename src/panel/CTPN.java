    private boolean kt() {
        if (txtsoluong.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Số Lượng", "Error", JOptionPane.ERROR_MESSAGE);
            txtsoluong.requestFocus();
            return false;
        } else if (txtgia.equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa Nhập Giá", "Error", JOptionPane.ERROR_MESSAGE);
            txtgia.requestFocus();
            return false;
        }
        return true;
    }

