    public void loaddata() {
        DefaultTableModel model = (DefaultTableModel) tab.getModel();
        model.setRowCount(0);
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, user, pass);
            String sql = "SELECT  MaHang,SoLuong,DonGia from Mat_Hang where MaNCC like '" + ncc + "'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                nv = new Vector();
                nv.add(rs.getString(1));
                nv.add(rs.getString(2));
                nv.add(rs.getString(3));
                model.addRow(nv);
            }
            con.close();
        } catch (Exception e) {
            System.out.println(e);
        }
//        System.out.println(ncc + 123);
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, user, pass);
            String sql = "select MaHang from Mat_Hang where MaNCC = '" + ncc + "'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            cbma.removeAllItems();
            while (rs.next()) {
                cbma.addItem(rs.getString(1));
            }
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

