    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
//        String a = txt1.getText();
//        String b = txt2.getText();
        Connection con = null;
        Statement stm = null;
        try {
            if (txtsearch.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "chưa nhập mã hàng", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                con = DriverManager.getConnection(url, user, pass);
                stm = con.createStatement();
                String query = "select * from Thong_Ke where MaHang= " + txtsearch.getText() + " and  (Thang BETWEEN  " + cb1.getSelectedItem() + " and " + cb2.getSelectedItem() + ")";
                ResultSet rs = stm.executeQuery(query);
                while (rs.next()) {
                    String t = "Thang " + rs.getString("Thang");
                    String c = rs.getString(3);
                    double soluong = Double.parseDouble(c);
                    dataset.setValue(soluong, "Soluongban", t);
                }
                JFreeChart barchart = ChartFactory.createLineChart("Số Lượng Bán Mặt Hàng Theo Tháng", "Tháng", "Chiếc", dataset, PlotOrientation.VERTICAL, true, true, false);
                BarRenderer renderer = null;
                CategoryPlot plot = null;
                renderer = new BarRenderer();
                ChartPanel barpanel = new ChartPanel(barchart);
                panelchart.removeAll();
                panelchart.add(barpanel, BorderLayout.CENTER);
                panelchart.validate();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

